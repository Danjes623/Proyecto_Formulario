-- 1. Tabla de tipos de documento (catálogo)
CREATE TABLE Tipos_Documento (
    Id_tipo_doc INT AUTO_INCREMENT PRIMARY KEY,
    codigo VARCHAR(5) NOT NULL UNIQUE COMMENT 'Código: CC, CE, PAS, NIT',
    nombre VARCHAR(50) NOT NULL COMMENT 'Nombre completo: Cédula de Ciudadanía, etc.',
    aplica_persona BOOLEAN DEFAULT TRUE COMMENT '¿Aplica para personas naturales?',
    aplica_empresa BOOLEAN DEFAULT FALSE COMMENT '¿Aplica para empresas?',
    activo BOOLEAN DEFAULT TRUE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE Representantes_Legales (
    Id_repleg INT AUTO_INCREMENT PRIMARY KEY COMMENT 'ID autogenerado para evitar duplicados',
    Tipo_doc VARCHAR(5) NOT NULL COMMENT 'CC, CE o PAS',
    Numero_documento VARCHAR(20) NOT NULL COMMENT 'Número de documento',
    Nom_repleg VARCHAR(100) NOT NULL,
    ape_repleg VARCHAR(100) NOT NULL,
    email_repleg VARCHAR(100) NOT NULL,
    Tel1_repleg VARCHAR(20) NOT NULL,  -- Cambiado a VARCHAR por teléfonos con formato
    activo BOOLEAN DEFAULT TRUE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    UNIQUE KEY unique_documento (Tipo_doc, Numero_documento),
    FOREIGN KEY (Tipo_doc) REFERENCES Tipos_Documento(codigo),
    INDEX idx_numero_documento (Numero_documento)
);

CREATE TABLE Entidades_Contratantes (
    Tipo_doc VARCHAR(5) DEFAULT 'NIT',
    Numero_documento VARCHAR(20) NOT NULL COMMENT 'NIT de la empresa',
    rsoc_entc VARCHAR(200) NOT NULL,
    dir_entc VARCHAR(200) NOT NULL,
    email_entc VARCHAR(100) NOT NULL,
    Tel1_entc VARCHAR(20) NOT NULL,
    Calt_entc VARCHAR(100),
    Ret_entc BOOLEAN NOT NULL DEFAULT FALSE COMMENT 'Es retenedor?',
    activo BOOLEAN DEFAULT TRUE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (Tipo_doc, Numero_documento),
    FOREIGN KEY (Tipo_doc) REFERENCES Tipos_Documento(codigo)
);

CREATE TABLE Usuarios (
    Id_usuario INT AUTO_INCREMENT PRIMARY KEY,
    username VARCHAR(50) NOT NULL UNIQUE,
    email VARCHAR(100) NOT NULL UNIQUE,
    password_hash VARCHAR(255) NOT NULL,
    Tipo_doc VARCHAR(5),
    Numero_documento VARCHAR(20),
    nombres VARCHAR(100),
    apellidos VARCHAR(100),
    rol ENUM('admin', 'usuario') DEFAULT 'usuario',
    activo BOOLEAN DEFAULT TRUE,
    last_login TIMESTAMP NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (Tipo_doc) REFERENCES Tipos_Documento(codigo)
);

CREATE TABLE Departamentos (
    Id_dep INT PRIMARY KEY,
    codigo_dane VARCHAR(2) NOT NULL UNIQUE,
    nom_dep VARCHAR(100) NOT NULL
);

CREATE TABLE Municipios (
    Id_mun INT PRIMARY KEY,
    codigo_dane VARCHAR(5) NOT NULL UNIQUE,
    nom_mun VARCHAR(100) NOT NULL,
    Id_dep INT NOT NULL,
    FOREIGN KEY (Id_dep) REFERENCES Departamentos(Id_dep),
    INDEX idx_departamento (Id_dep)
);

