CREATE TABLE Contratos (
    Num_cont INT PRIMARY KEY,
    Val_con DECIMAL(15,2) NOT NULL,
    Desc_cont DECIMAL(15,2) NOT NULL COMMENT 'Valor del descuento (2%)',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE Formularios (
    Id_form INT AUTO_INCREMENT PRIMARY KEY,
    Tipo_doc_repleg VARCHAR(5) NOT NULL,
    Numero_doc_repleg VARCHAR(20) NOT NULL,
    Tipo_doc_entc VARCHAR(5) NOT NULL,
    Numero_doc_entc VARCHAR(20) NOT NULL,
    Id_dep INT NOT NULL,
    Id_mun INT NOT NULL,
    Num_cont INT NOT NULL,
    Total_form DECIMAL(15,2) NOT NULL COMMENT 'Valor final después del descuento',
    fecha_registro TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    usuario_registra INT,
    FOREIGN KEY (Tipo_doc_repleg, Numero_doc_repleg) 
        REFERENCES Representantes_Legales(Tipo_doc, Numero_documento),
    FOREIGN KEY (Tipo_doc_entc, Numero_doc_entc) 
        REFERENCES Entidades_Contratantes(Tipo_doc, Numero_documento),
    FOREIGN KEY (Id_dep) REFERENCES Departamentos(Id_dep),
    FOREIGN KEY (Id_mun) REFERENCES Municipios(Id_mun),
    FOREIGN KEY (Num_cont) REFERENCES Contratos(Num_cont),
    FOREIGN KEY (usuario_registra) REFERENCES Usuarios(Id_usuario),
    INDEX idx_representante (Tipo_doc_repleg, Numero_doc_repleg),
    INDEX idx_empresa (Tipo_doc_entc, Numero_doc_entc)
);

